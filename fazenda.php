so de fabricação dos produtos é assegurado pela pasteurização e controle de qualidade em tempo integral, garantindo integridade do produto sem descaracterizar as particularidades nutricionais do ovo, é a primeira e única indústria de fabricação de ovo líquido pasteurizado da Região Norte.                     


