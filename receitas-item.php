                    <strong>Comer ovos todos os dias aumenta a taxa de colesterol no sangue.</strong><br/>
MITO: As pessoas saudáveis podem consumir um ovo por dia.  <br/><br/>

<strong>É errado guardar os ovos na porta da geladeira. </strong><br/>
VERDADE: O abre e fecha constante da porta facilita a alteração da temperatura, que prejudica o alimento. Além disso, o ato de bater a porta pode causar micro-rachaduras imperceptíveis ao olho humano, que podem contaminar o interior do ovo. Se a sua geladeira não tem um compartimento específico para os ovos, guarde-os na própria embalagem, na prateleira intermediária.<br/><br/>

<strong>É preciso lavar os ovos antes de guardá-los na geladeira. </strong><br/>
MITO: Os ovos da Fazenda São Pedro passam por um rígido controle de qualidade. Por isso, não há necessidade de limpá-los antes de usar, até porque o procedimento elimina a película natural que protege a casca, aumentando sua porosidade e tornando-a suscetível à contaminação por micro-organismos. Caso queira lavar os ovos mesmo assim, o recomendado é fazer isso no momento do uso.<br/><br/>

<strong>Há riscos em consumir o ovo cru ou com a gema mole.</strong><br/>
VERDADE: Se a cocção não é adequada, eventuais bactérias presentes no alimento podem sobreviver. A bactéria salmonela, por exemplo, pode se desenvolver em temperaturas de até 50 ºC e causa infecções. Gestantes, idosos, crianças e imunodeficientes têm mais risco de desenvolver formas mais graves de salmonelose e devem redobrar a atenção. A melhor forma de consumir ovos são Pedro é cozinhá-los por 12 minutos. É preciso atenção também a doces e gemadas preparados com ovos são Pedro crus.<br/><br/>

<strong>A clara de ovo traz benefícios para a saúde dos atletas. </strong><br/>
VERDADE: A clara não tem gordura e é riquíssima em um tipo de proteína chamada albumina. Seu consumo é indicado para os esportistas, depois do treino, para ajudar na recuperação e na restauração dos músculos. Porém, duas claras por dia fornecem a quantidade suficiente de proteínas.  

